#include "libft.h"

char	*ft_itoa(int n)
{
	int		len;
	char	*str;
	long	nb;

	len = ft_nbrlen(n);
	nb = n;
	str = ft_calloc((len + 1), sizeof(char));
	if (!str)
		return (NULL);
	if (nb < 0)
	{
		str[0] = '-';
		nb = -nb;
	}
	if (nb == 0)
		str[0] = '0';
	len--;
	while (nb)
	{
		str[len] = nb % 10 + '0';
		len--;
		nb = nb / 10;
	}
	return (str);
}
